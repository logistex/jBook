# ## 1.3 평가 계획
# 
# - 시험보다는 과제 위주로 평가
#     - 단편적 암기 위주 필기 시험의 문제점
#     - 필기 시험은 최대한 억제
#     - 과제를 통한 심층적 문제 해결 능력을 평가
#     - 평가 요소에 대한 사전 설명
# 
# 구분 | 배점 | 평가의 초점 |
# --- | ---: | :--- |
# 출석 |  15 | 성실성
# 평소 |  15 | 문제 해결 능력 및 의지 (예: 타이타닉 생존율 시각화)
# 중간 |  30 | 문제 해결 능력 및 의지 (예: 범죄 및 검거 시각화)
# 기말 |  40 | 문제 해결 능력 및 의지 (예: Covid19 시각화)
# 합계 | 100 | -
# 
# - 질의/응답은 e-Class System으로 단일화
